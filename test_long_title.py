"""
Тестовый скрипт для проверки обработки длинных заголовков и текста.
"""

from notes import Note, NoteStore

def test_long_content():
    """Создание заметки с очень длинным заголовком и текстом."""
    store = NoteStore()
    
    # Заметка с длинным заголовком (в пределах 100 символов)
    long_title = "Это длинный заголовок заметки который тестирует обрезку в списке заметок"
    long_body = "Это текст в одну очень длинную строку без переносов который также мог бы растянуть окно приложения если бы не было включено автоматическое переносов слов в текстовом редакторе что очень важно для удобства работы с приложением и предотвращения горизонтальной прокрутки"
    
    note = Note(title=long_title, body=long_body)
    store.add_note(note)
    
    print(f"✅ Создана заметка с длинным заголовком (ID: {note.id[:8]})")
    print(f"   Заголовок: {note.title}")
    print(f"   Длина заголовка: {len(note.title)} символов")
    print(f"   Длина текста: {len(note.body)} символов")

if __name__ == "__main__":
    test_long_content()
